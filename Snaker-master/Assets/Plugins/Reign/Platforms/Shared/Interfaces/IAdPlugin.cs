namespace Reign.Plugin
{
	/// <summary>
	/// Base Ad interface object
	/// </summary>
    public interface IAdPlugin
    {
		/// <summary>
		/// Show or Hide Ad
		/// </summary>
		bool Visible {get; set;}

		/// <summary>
		/// Dispose Ad
		/// </summary>
		void Dispose();

		/// <summary>
		/// Set Ad gravity
		/// </summary>
		/// <param name="gravity">Gravity type</param>
		void SetGravity(AdGravity gravity);

		/// <summary>
		/// Refresh Ad
		/// </summary>
		void Refresh();

		/// <summary>
		/// Update operations
		/// </summary>
		void Update();

		/// <summary>
		/// Classic gui operations
		/// </summary>
		void OnGUI();

		/// <summary>
		/// Used to manually render gui operations
		/// </summary>
		void OverrideOnGUI();
    }
}